package net.sf.gratia.storage;

public class CPUMetricTypes
{
		private int id;
		private String CPUMetricType;

		public CPUMetricTypes()
		{
		}

		public int getid()
		{
				return id;
		}

		public void setid(int value)
		{
				id = value;
		}

		public String getCPUMetricType() 
		{
				return CPUMetricType;
		}

		public void setCPUMetricType(String value) 
		{
				CPUMetricType = value;
		}
}
